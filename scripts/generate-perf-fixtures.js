#!/usr/bin/env node
import { mkdirSync, writeFileSync } from "node:fs";
import { join } from "node:path";

const root = process.cwd();
const outputDir = process.argv[2] || join(root, "target", "perf-fixtures");
const fixtures = [
  { name: "papyro-100kb.md", bytes: 100 * 1024 },
  { name: "papyro-1mb.md", bytes: 1024 * 1024 },
  { name: "papyro-5mb.md", bytes: 5 * 1024 * 1024 },
];

const seedBlocks = [
  ({ index }) => `## Section ${index}

This paragraph is part of a deterministic Papyro performance fixture. It keeps
the document close to real notes by mixing prose, short lists, and code blocks.

- Capture the current interaction budget.
- Switch tabs and view modes while the editor stays responsive.
- Keep preview and outline work away from the input path.

`,
  ({ index }) => "```rust\n" +
    `fn section_${index}_budget(bytes: usize) -> bool {\n` +
    "    bytes > 0 && bytes <= 5 * 1024 * 1024\n" +
    "}\n" +
    "```\n\n",
  ({ index }) => `> Note ${index}: fixture text is intentionally plain ASCII so byte sizes stay stable across platforms.

`,
];

function byteLength(content) {
  return Buffer.byteLength(content, "utf8");
}

function makeFixture(targetBytes) {
  let content = `# Papyro Performance Fixture

Generated by scripts/generate-perf-fixtures.js.

Target bytes: ${targetBytes}

`;
  let index = 1;

  while (byteLength(content) < targetBytes) {
    for (const block of seedBlocks) {
      content += block({ index });
      if (byteLength(content) >= targetBytes) break;
    }
    index += 1;
  }

  const currentBytes = byteLength(content);
  if (currentBytes > targetBytes) {
    content = content.slice(0, targetBytes);
  }

  if (byteLength(content) < targetBytes) {
    content += "x".repeat(targetBytes - byteLength(content));
  }

  return content;
}

mkdirSync(outputDir, { recursive: true });

for (const fixture of fixtures) {
  const path = join(outputDir, fixture.name);
  const content = makeFixture(fixture.bytes);
  writeFileSync(path, content, "utf8");
  console.log(`${fixture.name}: ${byteLength(content)} bytes`);
}

console.log(`Wrote fixtures to ${outputDir}`);
