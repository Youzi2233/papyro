#!/usr/bin/env node
import {
  mkdirSync,
  mkdtempSync,
  readFileSync,
  rmSync,
  writeFileSync,
} from "node:fs";
import { tmpdir } from "node:os";
import { join } from "node:path";

const root = process.cwd();
const defaultOutputDir = join(root, "target", "perf-fixtures");
const fixtures = [
  { name: "papyro-100kb.md", bytes: 100 * 1024 },
  { name: "papyro-1mb.md", bytes: 1024 * 1024 },
  { name: "papyro-5mb.md", bytes: 5 * 1024 * 1024 },
];

const seedBlocks = [
  ({ index }) => `## Section ${index}

This paragraph is part of a deterministic Papyro performance fixture. It keeps
the document close to real notes by mixing prose, short lists, and code blocks.

- Capture the current interaction budget.
- Switch tabs and view modes while the editor stays responsive.
- Keep preview and outline work away from the input path.

`,
  ({ index }) => "```rust\n" +
    `fn section_${index}_budget(bytes: usize) -> bool {\n` +
    "    bytes > 0 && bytes <= 5 * 1024 * 1024\n" +
    "}\n" +
    "```\n\n",
  ({ index }) => `> Note ${index}: fixture text is intentionally plain ASCII so byte sizes stay stable across platforms.

`,
];

function byteLength(content) {
  return Buffer.byteLength(content, "utf8");
}

function main() {
  const args = process.argv.slice(2);

  if (args.includes("--help") || args.includes("-h")) {
    printUsage();
    return;
  }

  if (args.includes("--self-test")) {
    runSelfTest();
    return;
  }

  const unexpectedOption = args.find((arg) => arg.startsWith("-"));
  if (unexpectedOption) {
    console.error(`Unknown option: ${unexpectedOption}`);
    printUsage();
    process.exitCode = 2;
    return;
  }

  writeFixtures(args[0] || defaultOutputDir);
}

function printUsage() {
  console.log(`Usage:
  node scripts/generate-perf-fixtures.js
  node scripts/generate-perf-fixtures.js <output-dir>
  node scripts/generate-perf-fixtures.js --self-test

Writes deterministic 100KB, 1MB, and 5MB Markdown files for manual
performance smoke testing.`);
}

function makeFixture(targetBytes) {
  const chunks = [];
  let currentBytes = 0;
  const append = (chunk) => {
    chunks.push(chunk);
    currentBytes += byteLength(chunk);
  };

  append(`# Papyro Performance Fixture

Generated by scripts/generate-perf-fixtures.js.

Target bytes: ${targetBytes}

`);
  let index = 1;

  while (currentBytes < targetBytes) {
    for (const block of seedBlocks) {
      const chunk = block({ index });
      const remainingBytes = targetBytes - currentBytes;
      const chunkBytes = byteLength(chunk);

      if (chunkBytes <= remainingBytes) {
        append(chunk);
      } else {
        append(chunk.slice(0, remainingBytes));
      }

      if (currentBytes >= targetBytes) break;
    }
    index += 1;
  }

  let content = chunks.join("");
  const finalBytes = byteLength(content);
  if (finalBytes > targetBytes) {
    content = content.slice(0, targetBytes);
  }

  if (byteLength(content) < targetBytes) {
    content += "x".repeat(targetBytes - byteLength(content));
  }

  return content;
}

function writeFixtures(outputDir) {
  mkdirSync(outputDir, { recursive: true });

  for (const fixture of fixtures) {
    const path = join(outputDir, fixture.name);
    const content = makeFixture(fixture.bytes);
    writeFileSync(path, content, "utf8");
    console.log(`${fixture.name}: ${byteLength(content)} bytes`);
  }

  console.log(`Wrote fixtures to ${outputDir}`);
}

function readFixtureBytes(outputDir, fixture) {
  return readFileSync(join(outputDir, fixture.name));
}

function runSelfTest() {
  const outputDir = mkdtempSync(join(tmpdir(), "papyro-perf-fixtures-"));

  try {
    writeFixtures(outputDir);

    for (const fixture of fixtures) {
      const actual = readFixtureBytes(outputDir, fixture);
      const expected = Buffer.from(makeFixture(fixture.bytes), "utf8");

      assert(
        actual.byteLength === fixture.bytes,
        `${fixture.name} should be ${fixture.bytes} bytes`,
      );
      assert(
        actual.equals(expected),
        `${fixture.name} should match deterministic fixture content`,
      );
    }

    console.log("Performance fixture generator self-test passed.");
  } finally {
    rmSync(outputDir, { recursive: true, force: true });
  }
}

function assert(condition, message) {
  if (!condition) {
    console.error(`Performance fixture generator self-test failed: ${message}`);
    process.exitCode = 1;
    throw new Error(message);
  }
}

main();
